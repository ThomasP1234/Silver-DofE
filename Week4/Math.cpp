#include <iostream>
#include <cmath>
using namespace std;

int main() {
	int x = 5;
	int y = 10;
	cout << max(x, y) << endl;
	cout << min(x, y) << endl;

	// C++ <cmath> Header Examples
	cout << sqrt(64) << endl;
	cout << round(2.6) << endl;
	cout << log(100) << endl;
	cout << pow(10, 2) << endl;
	cout << sin(45) << endl;
	cout << cos(45) << endl;
	cout << tan(45) << endl;

	return 0;
}
