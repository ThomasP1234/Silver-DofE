#include <iostream>
using namespace std;

int main() {
	int x = 100 + 50; // + adds values together
	cout << x << endl;
	int y = 100 - 5; // - subtracts a value from an other
	cout << y << endl;
	int z = 100 * 50; // * multiplies values together
	cout << z << endl;
	int a = 100 / 50; // / divides a value from an other
	cout << a << endl;
	int b = 100 % 50; // returns the division remainder
	cout << b << endl;
	int c = 10;
	cout << c << endl;
	++c; // ++ increments a value by one
	cout << c << endl;
	--c; // -- decreases a value bye one
	cout << c << endl;

	return 0;
}
